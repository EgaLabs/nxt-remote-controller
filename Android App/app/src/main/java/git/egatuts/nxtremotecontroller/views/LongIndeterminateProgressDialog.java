package git.egatuts.nxtremotecontroller.views;

import git.egatuts.nxtremotecontroller.activity.BaseActivity;

/*
 *  Progress dialog class used to show a progress dialog when the app is doing some
 *  fast operations that doesn't need to be canceled... because they're fast.
 */
public class LongIndeterminateProgressDialog extends BaseIndeterminateProgressDialog {

  /*
   *  Constructor.
   */
  public LongIndeterminateProgressDialog (BaseActivity activity) {
    super(activity);
    super.setCancelable(true);
  }

  /*
   *  Overridden method that sets the dialog as cancelable and sets doFirstAnimation flag to true.
   */
  @Override
  public void onPostShow () {
    super.onPostShow();
    super.setDoFirstAnimation(true);
  }

}
